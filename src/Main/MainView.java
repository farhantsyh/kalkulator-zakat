    private void hitungZakatHasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungZakatHasilActionPerformed
        //object
        ZakatPenghasilan Hasil = new ZakatPenghasilan();
        
        //set penghasilan
        String a = hasilField.getText();
        long hasil = Long.parseLong(a);
        Hasil.setHarta1(hasil);
                
        //set penghasilan lain
        String b = hasilLainField.getText();
        long hasilLain = Long.parseLong(b);
        Hasil.setHarta2(hasilLain);
        
        //set hutang
        String c = hasilHutangField.getText();
        long hutang = Long.parseLong(c);
        Hasil.setHutang(hutang);
        
        //hitung-get emas-nisab-total-zakat
        double hargaEmas = Hasil.getHargaEmas();
        long he = (long) hargaEmas;
        String emas = Long.toString(he);
        
        Hasil.hitungNisab();
        double nisabHasil = Hasil.getNisab();
        long nh = (long) nisabHasil;
        String nisab = Long.toString(nh);
        
        Hasil.hitungZakat();
        long totalZakat = Hasil.getTotal();
        String total = Long.toString(totalZakat);
        double zakatHasil = Hasil.getZakat();
        long zh = (long) zakatHasil;
        String zakat = Long.toString(zh);
        
        //tampil di label/text paragraph
        String wajib = Hasil.ketWajib();
        String tampil = Hasil.tampilZakat();
        ketHasilArea.setText(wajib + tampil);
        
        //tampil di field
        totalHasilField.setText(total);
        zakatHasilField.setText(zakat);
        emasHasilField.setText(emas);
        nisabHasilField.setText(nisab);
    }//GEN-LAST:event_hitungZakatHasilActionPerformed

